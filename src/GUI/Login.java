    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String Username = username.getText();
        String Password = password.getText();
        try{
            Statement stmt = koneksi.createStatement();
            String query = "SELECT * FROM user WHERE username='"+Username+"' && password='"+Password+"';";
            ResultSet rs = stmt.executeQuery(query);

            while (rs.next()){
                String roleUser = rs.getString("role");
                if ("Pegawai".equals(roleUser)) {
                    new HomePegawai().setVisible(true);
                } else if ("Pasien".equals(roleUser)) {
                    new PendaftaranPelanggan().setVisible(true);
                }
            }
            this.setVisible(false);
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Gagal Login, Karena: " + ex);
        }
        if (username.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Username Belum Diisi");
        } else if (password.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Password Belum Diisi");
            this.setVisible(true);
        }
    }//GEN-LAST:event_loginButtonActionPerformed

