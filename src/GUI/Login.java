    public void Huruf(KeyEvent a){
        if(Character.isDigit(a.getKeyChar())){
            a.consume();
            JOptionPane.showMessageDialog(null, "Masukkan Hanya Huruf", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }

    public void HurufKecil(KeyEvent d){
        if(Character.isUpperCase(d.getKeyChar())){
            d.consume();
            JOptionPane.showMessageDialog(null, "Masukkan Hanya Huruf Kecil", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }
